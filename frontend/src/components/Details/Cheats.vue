              <!-- Empty state message when no cheat files -->
              <v-alert v-else type="info" variant="tonal" class="mt-2">
                {{
                  t("rom.noCheatFiles") ||
                  "No cheat files uploaded yet. Use the input above to upload cheat files."
                }}
              </v-alert>
            </v-card>
          </v-tabs-window-item>
        </v-tabs-window>
      </v-col>
    </v-row>
  </v-card-text>
</template>

